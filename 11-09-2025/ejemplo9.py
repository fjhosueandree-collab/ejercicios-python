def calcula_media(x, y):
    resultado = (x + y) / 2
    return resultado

# Ejemplo: Promedio de temperaturas
media = calcula_media(y=20, x=30)
print(media)  # Output: 25.0
media = calcula_media(15, 25)
print(media)  # Output: 20.0
media = calcula_media(10, 40)
print(media)  # Output: 25.0
media = calcula_media(22, 28)
print(media)  # Output: 25.0
print("La temperatura promedio es:")
print(media)  # Output: La temperatura promedio es: 25.0
print("Programa terminado")

# Ejemplo 1: Saludo personalizado
nombre = "Ana"
print("Bienvenida, %s!" % nombre)  # Output: Bienvenida, Ana!

# Ejemplo 2: Información de un estudiante
estudiante = "Carlos"
curso = "Python"
print("Estudiante: %s, Curso: %s" % (estudiante, curso))  # Output: Estudiante: Carlos, Curso: Python

# Ejemplo 3: Dirección completa
persona = "María"
ciudad = "Lima"
pais = "Perú"
print("Hola %s, estás en %s, %s." % (persona, ciudad, pais))  # Output: Hola María, estás en Lima, Perú.

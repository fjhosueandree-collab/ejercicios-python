# Ejemplo 1: Una sola variable
x = "Juan"
print("Hola, %s!" % x)  # Output: Hola, Juan!

# Ejemplo 2: Múltiples variables
nombre = "Laura"
edad = 22
print("Usando %s" % nombre)  # Output: Usando Laura
print("%s tiene %s años." % (nombre, edad))  # Output: Laura tiene 22 años.

# Ejemplo 3: Más variables
var1 = "Juan!"
var2 = "Ciudad de Puno"
print("Hola %s estas en %s para trabajar?." % (var1, var2))  # Output: Hola Juan! estas en Ciudad de Puno para trabajar?.

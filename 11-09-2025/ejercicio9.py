def calcula_media(x, y):
    resultado = (x + y) / 2
    return resultado

media = calcula_media(y=5, x=3)
print(media)  # Output: 4.0
media = calcula_media(3, 5)
print(media)  # Output: 4.0
media = calcula_media(7, 30)
print(media)  # Output: 18.5
media = calcula_media(600, 800)
print(media)  # Output: 700.0
print("La media es:")
print(media)  # Output: La media es: 700.0

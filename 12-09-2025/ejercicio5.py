# aritmetica.py, matematica.py, matematica2.py y matematica_alternativa.py combinados
def sumar(a, b):
    return a + b

def restar(a, b):
    return a - b

def mult(a, b):
    return a * b

def div(a, b):
    return a / b

# Invocación como en matematica.py
print(sumar(7, 5))

# Invocación como en matematica2.py
print(sumar(7, 5))
print(restar(7, 5))
print(mult(7, 5))
print(div(7, 5))

# Invocación como en matematica_alternativa.py
print(sumar(7, 5))

# Lista de 5 provincias de Puno
provincias_puno = ["Azángaro", "Carabaya", "Huancané", "Puno", "San Román"]

# Lista de 5 provincias de España
provincias_españa = ["Barcelona", "Madrid", "Sevilla", "Valencia", "Zaragoza"]

# Usar extend() para añadir las provincias de España a la lista de provincias de Puno
provincias_puno.extend(provincias_españa)

# Imprimir la lista resultante
print(provincias_puno)

# Tupla con nombres (suponiendo un nombre genérico, puedes cambiarlo por el tuyo)
nombres = ("Juan", "María", "Juan", "Ana", "Pedro")
print("Tupla de nombres:", nombres)
# Usamos el método count() para contar cuántas veces aparece "Juan"
print("Cantidad de veces que aparece 'Juan':", nombres.count("Juan"))
# Usamos el método index() para encontrar el índice de "María"
print("Índice de 'María':", nombres.index("María"))

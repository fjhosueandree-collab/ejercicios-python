# Este programa muestra cómo se usan los valores booleanos (True y False) en Python
# y también cómo identificar su tipo de dato con la función type().

# aT significa "afirmación verdadera"
aT = True
print("El valor es Verdadero:", aT, ", el cual es de tipo", type(aT))  # Muestra el valor y su tipo (bool)

print()  # Línea en blanco para separar la salida

# aF significa "afirmación falsa"
aF = False
print("El valor es Falso:", aF, ", el cual es de tipo", type(aF))  # Muestra el valor y su tipo (bool)

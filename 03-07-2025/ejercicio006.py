print(type(-4.5))              # <class 'float'>
print(type(2.78))              # <class 'float'>
print(type('Hola'))            # <class 'str'>

print(isinstance(-4.5, float))     # True
print(isinstance(-4.5, int))       # False
print(isinstance(-4.5, bool))      # False
print(isinstance(False, bool))     # True
